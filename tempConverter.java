public class tempConverter {
    public static void main(String[] args){

        Scanner scan = new Scanner(System.in);

        //Greeting
        System.out.println("Hello! Welcome to the Fahrenheit to Celsius Converter! Here I will convert a given temperature " +
                "from Fahrenheit.");
        System.out.println("Please enter a temperature in Fahrenheit: ");
        double fTemp = scan.nextDouble();

        double cTemp = (5.0/9)*(fTemp - 32);

        System.out.println((Math.round(fTemp * 100.0)/100) + " converts to " + (Math.round(cTemp * 100.0)/100) + " degrees Celsius!");

    }
}
